int	ft_power_to(int nbr, int power)
{
	int	i;

	i = 0;
	if (power == 0 || nbr == 1)
		return (1);
	while (i != power - 1)
	{
		nbr = nbr * nbr;
		i++;
	}
	return (nbr);
}
