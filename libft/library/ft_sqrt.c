int	ft_sqrt(int nbr)
{
	int	i;

	i = 0;
	if (nbr == 0)
		return (0);
	if (nbr == 1)
		return (1);
	while (i * i < nbr)
		i++;
	if (i * i == nbr)
		return (i);
	return (-1);
}
